function generateSparkline(base: number, volatility: number): number[] {
  const points: number[] = [];
  let current = base;
  for (let i = 0; i < 7; i++) {
    current = current * (1 + (Math.random() - 0.5) * volatility);
    points.push(Math.max(0.01, current));
  }
  return points;
}

