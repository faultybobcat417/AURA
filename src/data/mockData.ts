function generateRegions(baseLatency: number, variance: number): RegionLatency[] {
  return regions.map(r => {
    const v = Math.random() * variance;
    const p50 = Math.round(baseLatency + v);
    const p90 = Math.round(p50 * (1.4 + Math.random() * 0.3));
    return {
      region: r,
      p50,
      p90,
      p99: Math.round(p50 * (2 + Math.random())),
      status: p50 > 500 ? 'outage' : p50 > 200 ? 'degraded' : 'operational',
    };
  });
}

